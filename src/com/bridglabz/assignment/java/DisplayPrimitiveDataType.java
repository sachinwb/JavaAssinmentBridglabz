package com.bridglabz.assignment.java;

public class DisplayPrimitiveDataType {
	
    byte b ;
	static short s ;
	static int i ;
	static long l ;
	static float f ;
	static double d ;
	static char ch ;
	static boolean bn ;
	
	
    //Write a Java program to display the default value of all primitive data types of Java
	public static void main(String[] args) {
		DisplayPrimitiveDataType d = new DisplayPrimitiveDataType();
		
		
		
		System.out.println("The byte primitive data type is : "+d.b);
		System.out.println("The short primitive data type is : "+d.s);
		System.out.println("The int primitive data type is : "+i);
		System.out.println("The long primitive data type is : "+l);
		System.out.println("The float primitive data type is : "+f);
		System.out.println("The double primitive data type is : "+d);
		System.out.println("The char primitive data type is : "+ch);
		System.out.println("The boolean primitive data type is : "+bn);

	}

}
